    public function testIsSolvedGetsSetAppropriately()
    {
        $sudoku = new Sudoku();

        $this->assertEquals($sudoku->isSolved(), false);
        $sudoku->solve();
        $this->assertEquals($sudoku->isSolved(), true);
        $sudoku->setPuzzle();
        $this->assertEquals($sudoku->isSolved(), false);
        $sudoku->solve();
        $this->assertEquals($sudoku->isSolved(), true);
        $sudoku->generatePuzzle();
        $this->assertEquals($sudoku->isSolved(), false);
    }

